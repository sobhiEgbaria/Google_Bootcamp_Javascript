/*
  Determine if the width is greater than the height (landscape)
  should return true or false
 */
function isLandscape(width, height) {
  if (width > height) {
    return true;
  }
  return false;
}

// Usage example:
let checkWidthHeight1 = isLandscape(800, 600);
console.log("Landscape:", checkWidthHeight1); // should return true

let checkWidthHeight2 = isLandscape(600, 800);
console.log("Landscape:", checkWidthHeight2); // should return false

let checkWidthHeight3 = isLandscape(1024, 768);
console.log("Landscape:", checkWidthHeight3); // should return true
